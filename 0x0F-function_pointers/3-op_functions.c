/**
 * op_add - adds two integers
 * @a: integer
 * @b: integer
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - performs substrction operation on two integers
 * @a: integer
 * @b: integer
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two integers
 * @a: integer
 * @b: integer
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - performs integer division on two numbers
 * @a: integer
 * @b: integer
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod- gets the remainder
 * @a: integer
 * @b: integer
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
